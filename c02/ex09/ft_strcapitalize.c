char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (((str[i] >= 'a' && str[i] <= 'z') && (i == 0)))
		{
			str[i] = str[i] - 32;
		}
		if ((str[i + 1] >= 'A' && str[i + 1] <= 'Z'))
		{
			str[i + 1] = str[i + 1] + 32;
		}
		if (!((str[i] >= 'a' && str[i] <= 'z')
				|| (str[i] >= 'A' && str[i] <= 'Z')
				|| (str[i] >= '0' && str[i] <= '9')))
		{
			if ((str[i + 1] >= 'a' && str[i + 1] <= 'z'))
			{
				str[i + 1] = str[i + 1] - 32;
			}
		}
		i++;
	}
	return (str);
}
