char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (dest[i])
	{
		i++;
	}
	while (src[a])
	{
		dest[i++] = src[a];
		a++;
	}
	dest[i] = '\0';
	return (dest);
}
