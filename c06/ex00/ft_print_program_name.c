#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*a;

	argc = 0;
	a = argv[0];
	i = 0;
	while (a[i])
	{
		write(1, &a[i], 1);
		i++;
	}
	write (1, "\n", 1);
	return (0);
}
